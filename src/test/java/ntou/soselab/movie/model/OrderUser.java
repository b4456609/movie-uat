package ntou.soselab.movie.model;


public class OrderUser {
    private String name;
    private String phone;

    public OrderUser() {
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getPhone() {
        return phone;
    }

    public void setPhone(String phone) {
        this.phone = phone;
    }
}
